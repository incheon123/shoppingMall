package com.beans;

public class ProductSize {
	private String pid;
	private String psize;
	private String is_soldout;
	private int remain_quantity;
	
	
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getPsize() {
		return psize;
	}
	public void setPsize(String psize) {
		this.psize = psize;
	}
	public String getIs_soldout() {
		return is_soldout;
	}
	public void setIs_soldout(String is_soldout) {
		this.is_soldout = is_soldout;
	}
	public int getRemain_quantity() {
		return remain_quantity;
	}
	public void setRemain_quantity(int remain_quantity) {
		this.remain_quantity = remain_quantity;
	}
	
	
}	
